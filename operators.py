# Arithmetic operators
x = 10
y = 4
print(x + y)
print(x - y)
print(x / y)
print(x * y)
print(x % y)  # modulus-Returns a reminder after dividing two numbers

# Comparison Operators
a = 9
b = 78
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)  # equal to
print(a != b)  # not equal to

# Assignment Operators
w = 7
print(w)

y = 12
y += 3
print(y)

z = 9
z -= 5
print(z)

# Logical Operators- and, or not
num1 = 45
num2 = 34
print( num1 < num2 and num2 < num1) # returns a True if both statements are true
print( num1 < num2 or num2 < num1)
print(not (num1 < num2 or num2 < num1))


















